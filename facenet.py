def decov_loss(xs):
    """Decov loss as described in https://arxiv.org/pdf/1511.06068.pdf
    'Reducing Overfitting In Deep Networks by Decorrelating Representation'
    """
    x = tf.reshape(xs, [int(xs.get_shape()[0]), -1])
    m = tf.reduce_mean(x, 0, True)
    z = tf.expand_dims(x-m, 2)
    corr = tf.reduce_mean(tf.matmul(z, tf.transpose(z, perm=[0,2,1])), 0)
    corr_frob_sqr = tf.reduce_sum(tf.square(corr))
    corr_diag_sqr = tf.reduce_sum(tf.square(tf.diag_part(corr)))
    loss = 0.5*(corr_frob_sqr - corr_diag_sqr)
    return loss 


def dlib_metric_loss(anchor, positive, negative, dist_th, margin=0.04, squared=False):
    """Calculate the dlib metric loss
    
    Args:
      anchor: the embeddings for the anchor images.
      positive: the embeddings for the positive images.
      negative: the embeddings for the negative images.
      dist_th: distance threshold for metric loss
      margin: margin for metric loss
      squared: Boolean. If true, output is the pairwise squared euclidean distance matrix.
                 If false, output is the pairwise euclidean distance matrix. 
    Returns:
      the metric loss according to DLIB as a float tensor.
    """
    with tf.variable_scope('dlib_metric_loss'):
        # squared norms of each row in anchor, positive and negative
        pos_dist_squared = tf.reduce_sum(tf.square(tf.subtract(anchor, positive)), 1)
        neg_dist_squared = tf.reduce_sum(tf.square(tf.subtract(anchor, negative)), 1)
 
        # Get the mask where the zero distances are at.
        error_mask_pos = math_ops.less_equal(pos_dist_squared, 0.0)
        error_mask_neg = math_ops.less_equal(neg_dist_squared, 0.0)

        # Optionally take the sqrt.
        if squared:
            pos_dist = pos_dist_squared
            neg_dist = neg_dist_squared
        else:
            pos_dist = math_ops.sqrt(pos_dist_squared + math_ops.cast(error_mask_pos, dtypes.float32) * 1e-10)
            neg_dist = math_ops.sqrt(neg_dist_squared + math_ops.cast(error_mask_neg, dtypes.float32) * 1e-10)

        alpha1 = dist_th - margin
        alpha2 = dist_th + margin
        basic_positive_loss = tf.maximum(tf.subtract(pos_dist, alpha1), 0.0)
        basic_negative_loss = tf.maximum(tf.subtract(alpha2, neg_dist), 0.0)
        loss_positive = 0.5 * tf.reduce_mean(basic_positive_loss, 0)
        loss_negative = 0.5 * tf.reduce_mean(basic_negative_loss, 0)
        loss = tf.add(loss_positive, loss_negative)
      
    return loss
 
 
def center_loss(features, label, alfa, nrof_classes):
    """Center loss based on the paper "A Discriminative Feature Learning Approach for Deep Face Recognition"
       (http://ydwen.github.io/papers/WenECCV16.pdf)
    """
    nrof_features = features.get_shape()[1]
    centers = tf.get_variable('centers', [nrof_classes, nrof_features], dtype=tf.float32,
        initializer=tf.constant_initializer(0), trainable=False)
    label = tf.reshape(label, [-1])
    centers_batch = tf.gather(centers, label)
    diff = (1 - alfa) * (centers_batch - features)
    centers = tf.scatter_sub(centers, label, diff)
    with tf.control_dependencies([centers]):
        loss = tf.reduce_mean(tf.square(features - centers_batch))
    return loss, centers

